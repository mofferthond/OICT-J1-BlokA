    <script>
        function showInput(){
            document.getElementById('new-item').style.display = 'none';
            document.getElementById('new-item-form').style.display = 'block';
        }

        function confirmDelete(id){
            
            var button = document.getElementById('delete-button-' + id);
            button.innerHTML = 'weet je het zeker?';
            button.style.color = '#17252A';
            document.getElementById('yes-delete-' + id).style.opacity = '1';
            document.getElementById('no-delete-' + id).style.opacity = '1';
            document.getElementById('yes-delete-' + id).style.visibility = 'visible';
            document.getElementById('no-delete-' + id).style.visibility = 'visible';
        }

        function cancelDelete(id){
            var button = document.getElementById('delete-button-' + id);
            button.innerHTML = 'verwijderen';
            button.style.color = '#e76f51';
            document.getElementById('yes-delete-' + id).style.opacity = '0';
            document.getElementById('no-delete-' + id).style.opacity = '0';
            document.getElementById('yes-delete-' + id).style.visibility = 'hidden';
            document.getElementById('no-delete-' + id).style.visibility = 'hidden';
        }

        function showAvatarForm(){
            document.getElementById('profile-image-form').style.display = 'block';
        }

        function hideAvatarForm(){
            document.getElementById('profile-image-form').style.display = 'none';
        }

    </script>
